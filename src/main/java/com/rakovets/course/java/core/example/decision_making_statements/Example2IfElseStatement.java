package com.rakovets.course.java.core.example.decision_making_statements;

public class Example2IfElseStatement {
    public static void main(String[] args) {
        int number = 0;

        if (number > 0) {
            System.out.println("This number is positive");
        } else {
            System.out.println("This number is not positive");
        }
        System.out.println("This statement after if-else statement");
    }
}
